/**
 * POST /deploy
 *
 * Body (JSON):
 * {
 *   slug:   string   — URL-safe identifier (e.g. "alex-morgan")
 *   name:   string   — Display name
 *   html:   string   — The rendered portfolio innerHTML from the frontend
 *   theme:  string   — Active theme key ("developer"|"minimal"|"creative"|"designer")
 *   wizard: {        — Wizard selections (for metadata)
 *     industry: string | null,
 *     palette:  string | null,
 *     font:     string | null,
 *   }
 * }
 *
 * Response:
 * { url: "https://{username}.github.io/portfolio-hub/portfolios/{slug}" }
 */
app.post('/deploy', async (req, res) => {
  const { slug, name, html, theme = 'developer', wizard = {} } = req.body;

  // ── Validate ──────────────────────────────────────────────
  if (!slug || typeof slug !== 'string' || !/^[a-z0-9-]+$/.test(slug)) {
    return res.status(400).json({ error: 'Invalid or missing slug. Must be lowercase letters, numbers, and hyphens only.' });
  }
  if (!html || typeof html !== 'string') {
    return res.status(400).json({ error: 'Missing html payload.' });
  }
  if (html.length > 8_000_000) { // 8MB safety cap
    return res.status(413).json({ error: 'HTML payload too large (max 8MB).' });
  }

  console.log(`🚀  Deploying portfolio: ${slug} (theme: ${theme})`);

  try {
    // 1. Ensure the repo exists
    await ensureRepo();

    // 2. Build the standalone HTML page
    const pageHTML = buildStandaloneHTML(slug, name, html, theme, wizard);

    // 3. Write the file to portfolios/{slug}/index.html
    const filePath = `portfolios/${slug}/index.html`;
    await putFile(
      filePath,
      pageHTML,
      `🚀 Deploy portfolio: ${name} (${slug})`
    );
    console.log(`✅  Written: ${filePath}`);

    // 4. Ensure GitHub Pages is enabled
    await ensurePages();

    // 5. Return the live URL
    const liveURL = `https://${GITHUB_USERNAME}.github.io/${REPO_NAME}/portfolios/${slug}`;
    console.log(`🌐  Live at: ${liveURL}`);

    return res.json({ url: liveURL, slug, repo: REPO_NAME });

  } catch (err) {
    console.error('❌  Deploy error:', err.message || err);
    return res.status(500).json({
      error: err.message || 'Deployment failed. Check server logs.',
    });
  }
});

