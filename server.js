/**
 * Ensure the `portfolio-hub` repository exists.
 * Creates it as a public repo if it doesn't.
 * Returns the repo data object.
 */
async function ensureRepo() {
  try {
    const { data } = await octokit.repos.get({
      owner: GITHUB_USERNAME,
      repo:  REPO_NAME,
    });
    return data;
  } catch (err) {
    if (err.status !== 404) throw err;

    // Repo doesn't exist — create it
    console.log(`📦  Creating repo: ${GITHUB_USERNAME}/${REPO_NAME}`);
    const { data } = await octokit.repos.createForAuthenticatedUser({
      name:          REPO_NAME,
      description:   'Portfolios generated by Folio — folio.app',
      private:       false,
      auto_init:     true,   // creates an initial commit so Pages can be enabled
      has_issues:    false,
      has_projects:  false,
      has_wiki:      false,
    });
    return data;
  }
}

