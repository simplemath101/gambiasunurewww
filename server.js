/** Minimal HTML entity escaping for attribute values */
function escapeHTML(str) {
  return String(str || '')
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}

/**
 * Create or update a file in the repo using the GitHub Contents API.
 * If the file already exists, its SHA is required to update it.
 */
async function putFile(path, content, message) {
  // Try to get existing file SHA
  let sha;
  try {
    const { data } = await octokit.repos.getContent({
      owner: GITHUB_USERNAME,
      repo:  REPO_NAME,
      path,
    });
    sha = data.sha;
  } catch (err) {
    if (err.status !== 404) throw err;
    // File doesn't exist yet — sha stays undefined (create mode)
  }

  await octokit.repos.createOrUpdateFileContents({
    owner:   GITHUB_USERNAME,
    repo:    REPO_NAME,
    path,
    message,
    content: Buffer.from(content, 'utf8').toString('base64'),
    ...(sha ? { sha } : {}),
    branch:  PAGES_BRANCH,
  });
}

/**
 * Enable GitHub Pages on the repo (source: main branch, / root).
 * Silently ignores 409 (already enabled) errors.
 */
async function ensurePages() {
  try {
    await octokit.repos.createPagesSite({
      owner:  GITHUB_USERNAME,
      repo:   REPO_NAME,
      source: { branch: PAGES_BRANCH, path: '/' },
    });
    console.log('📄  GitHub Pages enabled');
  } catch (err) {
    // 409 = Pages already configured, 422 = same — both are fine
    if (err.status !== 409 && err.status !== 422) {
      console.warn('⚠️   Pages setup warning:', err.message);
    }
  }
}

